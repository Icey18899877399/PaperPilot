"""「AI 切片解析」提示词工程（结构化切片解释）。

结构
====
每种切片的系统提示词 = 共享底座（角色 + 防虚构红线，``EXPLANATION_BASE_RULES``）
                     + 该类型专属解读要点（``directive``，随切片类型变化）。

为什么按类型分
--------------
图片/图表/表格/公式/代码/列表/正文各有不同的"该看什么、怎么说才准"：
表格要读行列与数值、公式要逐符号解释、图片/图表受"文本模型看不到像素"的限制
需触发"信息不足"兜底。用一段笼统提示词无法兼顾，故按 ``chunk.kind`` 分派不同提示词。
对齐 docx 验收：US-05 图片证据不足须提示"图片信息不足"、US-06 表格不得编造数值、
US-07 公式保留 LaTeX 且未定义符号需标注。

新增/调整类型
-------------
在 ``EXPLANATION_PROMPTS`` 增改条目即可，Agent 无需改动；未注册的 kind 回落到正文。
"""

from __future__ import annotations

from dataclasses import dataclass

# ---------------------------------------------------------------------------
# 共享底座：角色 + 全类型通用的防虚构红线与输出格式
# ---------------------------------------------------------------------------
EXPLANATION_BASE_RULES = """
【通用规则（所有类型都必须遵守）】
- 你是论文视觉内容分析 Agent，为读者解释论文中的结构化切片。
- 只能依据给定的切片内容与提供的上下文作答；两者都没有的信息一律不得编造或臆测。
- 涉及具体数值、模型名、数据集名时必须逐字取自切片或上下文，禁止自行计算、换算或估计。
- 证据不足以支撑解释时，明确说明"信息不足"，不要用常识硬凑。
- 用简体中文，先说内容概要、再说关键要点、最后给读者应关注的结论；控制在 260 字以内；
  只输出纯文本，不使用 Markdown 标题、星号加粗或代码块。
""".strip()


@dataclass(frozen=True)
class ExplanationPromptSpec:
    """一种切片类型的解释提示词规格。"""

    kind: str
    label: str
    directive: str

    @property
    def system_prompt(self) -> str:
        # 顺序：类型专属要点 → 共享底座（红线置末以强调）
        return f"{self.directive}\n\n{EXPLANATION_BASE_RULES}"


# ---------------------------------------------------------------------------
# 各切片类型的专属解读要点
# ---------------------------------------------------------------------------
EXPLANATION_PROMPTS: dict[str, ExplanationPromptSpec] = {
    spec.kind: spec
    for spec in (
        ExplanationPromptSpec(
            kind="image",
            label="图片",
            directive=(
                "【图片切片解读要点】\n"
                "- 说明这张图展示了什么（实验装置、系统架构、方法流程或定性示例）。\n"
                "- 结合图题、脚注与相邻正文，说明图中主要元素/模块及其关系、作者想借此传达的结论。\n"
                "- 硬约束：你只能看到图题与文字描述、看不到图像像素本身。当图题与上下文不足以判断"
                "图片内容时，必须回答\"图片信息不足，无法据此解释\"，绝不凭空描述画面细节或颜色。"
            ),
        ),
        ExplanationPromptSpec(
            kind="chart",
            label="图表",
            directive=(
                "【图表切片解读要点】\n"
                "- 依据图题与上下文判断图表类型（折线/柱状/散点等）、坐标轴含义与单位。\n"
                "- 说明各曲线/柱/系列代表什么，指出主要趋势（上升、下降、交叉、峰值、拐点）及其含义。\n"
                "- 给出作者希望通过该图表得出的结论。\n"
                "- 硬约束：看不到图像像素；当图题与上下文不足以判断走向时，明确说明\"图表信息不足\"，"
                "不要编造具体趋势或数值。"
            ),
        ),
        ExplanationPromptSpec(
            kind="table",
            label="表格",
            directive=(
                "【表格切片解读要点】\n"
                "- 识别行标题、列标题及单位，说明这张表在对比什么。\n"
                "- 指出最大值、最小值与关键差异；说明加粗、下划线等标记通常代表的含义（如最优结果）。\n"
                "- 引用数值时逐字取自表格内容，绝不生成表中不存在的数字。\n"
                "- 表格识别不完整（缺行列或数值缺失）时，说明可辨认的部分并提示信息不完整。"
            ),
        ),
        ExplanationPromptSpec(
            kind="equation",
            label="公式",
            directive=(
                "【公式切片解读要点】\n"
                "- 先保留并复述原始 LaTeX 表达，再说明公式的整体用途/作用。\n"
                "- 逐个解释公式中的符号，但只解释在切片或上下文中已被定义的变量含义；\n"
                "- 原文未定义的符号必须标注为\"论文当前上下文未定义\"，不得假设其单位或取值范围。\n"
                "- 结合前后文说明该公式在方法中的位置与作用。"
            ),
        ),
        ExplanationPromptSpec(
            kind="code",
            label="代码",
            directive=(
                "【代码切片解读要点】\n"
                "- 说明这段代码/伪代码实现了什么功能、关键步骤或核心逻辑。\n"
                "- 指出主要输入、输出与关键变量；不臆测切片中未给出的实现细节。"
            ),
        ),
        ExplanationPromptSpec(
            kind="list",
            label="列表",
            directive=(
                "【列表切片解读要点】\n"
                "- 概括该列表的主题，说明各项之间的关系（并列、递进、分类或先后步骤）。\n"
                "- 提炼列表想强调的要点，不逐条照抄原文。"
            ),
        ),
        ExplanationPromptSpec(
            kind="text",
            label="正文",
            directive=(
                "【正文切片解读要点】\n"
                "- 概括这段正文的核心论点或信息，说明其中的关键术语、数据或因果关系。\n"
                "- 指出这段内容在论文论证中的作用，以及读者应重点关注的结论。"
            ),
        ),
    )
}

DEFAULT_EXPLANATION_KIND = "text"


def get_explanation_prompt(kind: str) -> ExplanationPromptSpec:
    """按切片类型取（纯文本路径）解释提示词；未注册的类型回落到正文。"""
    return EXPLANATION_PROMPTS.get(kind, EXPLANATION_PROMPTS[DEFAULT_EXPLANATION_KIND])


# ---------------------------------------------------------------------------
# 视觉（多模态）路径专用提示词：图片/图表随消息附带真实图像，模型能看到像素，
# 因此去掉纯文本路径"看不到像素"的约束，改为"依据图像内容作答"。
# ---------------------------------------------------------------------------
VISION_EXPLANATION_PROMPTS: dict[str, ExplanationPromptSpec] = {
    spec.kind: spec
    for spec in (
        ExplanationPromptSpec(
            kind="image",
            label="图片",
            directive=(
                "【图片切片解读要点（你能看到这张图片）】\n"
                "- 依据图像内容说明这张图展示了什么（实验装置、系统架构、方法流程或定性示例）。\n"
                "- 说明图中主要模块/元素及其关系（如架构图的模块与数据流、流程图的先后步骤）、"
                "坐标轴/图例含义、作者想借此传达的结论。\n"
                "- 以图像内容为准并结合图题与上下文；图中看不清的细节不要臆造，"
                "涉及精确数值时以图题/正文为准，不要编造具体数字。"
            ),
        ),
        ExplanationPromptSpec(
            kind="chart",
            label="图表",
            directive=(
                "【图表切片解读要点（你能看到这张图表）】\n"
                "- 依据图像说明图表类型（折线/柱状/散点等）、坐标轴含义与单位、各曲线/柱/系列代表什么。\n"
                "- 指出主要趋势（哪条更高、上升/下降、峰值、交叉、拐点）及其含义，给出作者的结论。\n"
                "- 趋势以你看到的图像为准；若某个精确数值在图中看不清，以图题/正文为准，不要编造具体数字。"
            ),
        ),
    )
}


def get_vision_prompt(kind: str) -> ExplanationPromptSpec | None:
    """取视觉路径提示词；仅图片/图表有，返回None表示该类型不走视觉。"""
    return VISION_EXPLANATION_PROMPTS.get(kind)
